function creatureSayCallback(cid, type, msg)
    if not npcHandler:isFocused(cid) then
        return false
    end

    local shopWindow = {}
    

    local function onBuy(cid, item, subType, amount, ignoreCap, inBackpacks)
        	
		if doPlayerRemoveItem(cid, 13191, amount*items[item].buy) then
			doPlayerAddItem(cid, item, amount*items[item].cantidad)		
		end
        player:getPosition():sendMagicEffect(12)
        return true
    end
	
	local function onSell(cid, item, subType, amount, ignoreCap, inBackpacks)
        
		
		if doPlayerRemoveItem(cid, item, amount) then
			doPlayerAddItem(cid, 13191, items[item].sell*amount)		
		end		        
        player:getPosition():sendMagicEffect(12)
        return true
    end

	
	local SelectCategoria = ""
    if msgcontains(msg, "trade") then
        selfSay("let's make a Deal... select one category: {ammo}, {axe}, {backpack}, {body}, {club}, {decoration}, {distance}, {feet}, {fluids}, {head}, {legs}, {necklace}, {quivers}, {recreative}, {ring}, {runes}, {scrolls}, {shield}, {sword}, {vip}, {wand}", cid)
		return true
	else	
        for var, item in pairs(items) do
			if msg == item.categoria then			
				if item.tipo == "Vender" then
					table.insert(shopWindow, {id = var, subType = 0, buy = 0, sell = item.sell, name = item.name})
				else
					table.insert(shopWindow, {id = var, subType = 0, buy = item.buy, sell = 0, name = item.name})
				end	
			end    
        end
        openShopWindow(cid, shopWindow, onBuy, onSell)
    end
    return true
end



